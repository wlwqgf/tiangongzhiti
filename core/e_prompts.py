# 评审结论映射（知识库 2.2）
CONCLUSION_RULES = [
    {"min": 80, "type": "通过", "desc": "≥80 分：通过"},
    {"min": 65, "type": "有条件通过", "desc": "65-79 分：有条件通过"},
    {"min": 50, "type": "不通过-可修改后重提", "desc": "50-64 分：不通过，可修改后重新申报"},
    {"min": 35, "type": "不通过-建议降级", "desc": "35-49 分：不通过，建议降低申报等级"},
    {"min": 0, "type": "不通过-重大缺陷", "desc": "<35 分：不通过，存在重大缺陷"},
]


def conclusion_of(total: float) -> dict:
    """按结论映射返回结论类型与说明。"""
    for rule in CONCLUSION_RULES:
        if total >= rule["min"]:
            return {"type": rule["type"], "desc": rule["desc"]}
    return CONCLUSION_RULES[-1]


