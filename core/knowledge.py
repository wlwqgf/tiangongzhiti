def search_policy(policy_name: str, keyword: str) -> Optional[dict]:
    doc = POLICIES.get(policy_name)
    if not doc:
        return None
    results = {k: v for k, v in doc["articles"].items() if keyword in k or keyword in v}
    return {"doc": doc, "results": results}


def find_cases(industry: str | None = None):
    if not industry:
        return CASES
    matches = [c for c in CASES if c["industry"] == industry]
    return matches or CASES


